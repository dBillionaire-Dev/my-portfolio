import { z } from 'zod';
import { insertUserSchema, insertProjectSchema, insertMessageSchema, insertBlogPostSchema, users, projects, messages, blogPosts } from './schema';

export const errorSchemas = {
  validation: z.object({
    message: z.string(),
    field: z.string().optional(),
  }),
  notFound: z.object({
    message: z.string(),
  }),
  unauthorized: z.object({
    message: z.string(),
  }),
  internal: z.object({
    message: z.string(),
  }),
};

